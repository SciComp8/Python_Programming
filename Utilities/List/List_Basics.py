# List is an ordered sequences of objects
# It can take a mix of different types of objects
# It is mutable

city = ['Tokyo', 'Shanghai', 'New York City', 'Los Angeles', 'Paris', 'London']
city[0] = 'Cairo' 
city[0:2] = ['Tokyo', 'Chicago']

# View the length of list
len(city)

# Perform arithmetic operations on the list elements
numbers = [1, 2, 5, 0, 2, 9, 10]
sum(numbers)
max(numbers)
